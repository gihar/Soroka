"""Нормализация принятой записи в состоянии диалога.

Обе точки приёма записи — файл (`media_handler`) и ссылка (`_process_url`) —
регистрируют новую запись через один хелпер, чтобы прежняя запись не осталась
в FSM-состоянии и не «протекла» в обработку. Список ключей записи задан здесь
единожды и не дублируется в точках приёма.
"""

from aiogram.fsm.context import FSMContext

# Ключи состояния, описывающие одну принятую запись. При приёме новой записи
# все они сбрасываются, чтобы прежняя запись (файл или скачанная ссылка) не
# осталась в состоянии рядом с новой.
RECORD_KEYS = ("file_id", "file_path", "file_url", "is_external_file")


async def register_new_record(state: FSMContext, **values) -> None:
    """Зарегистрировать новую запись в состоянии, вытеснив прежнюю.

    Сбрасывает все ключи записи (`RECORD_KEYS`) в ``None`` и следом применяет
    переданные значения — одним ``update_data``. Прочие ключи состояния
    (участники, тема встречи и т. п.) не затрагиваются.
    """
    reset = {key: None for key in RECORD_KEYS}
    await state.update_data(**{**reset, **values})
