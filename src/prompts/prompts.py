def build_generation_prompt(
    transcription: str,
    template_variables: Dict[str, str],
    speaker_mapping: Optional[Dict[str, str]] = None,
    meeting_type: str = "general",
    # New context parameters
    meeting_agenda: Optional[str] = None,
    project_list: Optional[str] = None
) -> str:
    """
    Создает промпт для второго запроса:
    - Извлечение данных протокола по переменным шаблона
    - Использование диаризации и сопоставления спикеров

    Args:
        transcription: Текст транскрипции (с диаризацией если есть)
        template_variables: Переменные шаблона (ключ -> описание)
        speaker_mapping: Сопоставление спикеров (SPEAKER_N -> Имя)
        meeting_type: Тип встречи
        meeting_agenda: Повестка встречи
        project_list: Список проектов

    Returns:
        Промпт для LLM
    """
    # Формируем описание полей
    variables_str = "\n".join([f"- {key}: {desc}" for key, desc in template_variables.items()])

    # Формируем информацию о спикерах
    mapping_info = ""
    if speaker_mapping:
        mapping_str = "\n".join([f"{k} = {v}" for k, v in speaker_mapping.items()])
        mapping_info = f"""
СОПОСТАВЛЕНИЕ СПИКЕРОВ:
{mapping_str}

ВАЖНО: В транскрипции используются метки SPEAKER_N. Заменяй их на реальные имена из списка выше при формировании протокола.
"""

    # Получаем специализированные инструкции для типа встречи
    type_instructions = _get_type_specific_instructions(meeting_type)
    formatting_instructions = _get_type_specific_formatting_instructions(meeting_type)
    field_rules = _build_field_specific_rules(template_variables)

    prompt = f"""Извлеки данные из транскрипции для заполнения протокола встречи.

ТИП ВСТРЕЧИ: {meeting_type}

{mapping_info}

ПОЛЯ ДЛЯ ИЗВЛЕЧЕНИЯ:
{variables_str}

ИНСТРУКЦИИ ПО ИЗВЛЕЧЕНИЮ:
1. Используй ТОЛЬКО информацию из транскрипции.
2. Если информация для поля отсутствует, пиши "Не указано".
3. Все значения должны быть строками. Списки оформляй через дефис и перенос строки (\\n).
4. Имена участников пиши в формате 'Имя Фамилия' (без отчества).

{type_instructions}

{formatting_instructions}

СПЕЦИФИЧНЫЕ ПРАВИЛА ПОЛЯМ:
{field_rules}"""

    # Add context section if provided
    if meeting_agenda or project_list:
        context_info = {
            'meeting_agenda': meeting_agenda,
            'project_list': project_list
        }

        # Filter out None values
        context_info = {k: v for k, v in context_info.items() if v}

        if context_info:
            prompt += "\n\n## ДОПОЛНИТЕЛЬНЫЙ КОНТЕКСТ ДЛЯ АНАЛИЗА\n\n"
            prompt += "Используй следующую информацию для более точного извлечения данных протокола:\n\n"

            for key, value in context_info.items():
                if key == 'meeting_agenda':
                    prompt += f"**Повестка встречи:**\n{value}\n\n"
                elif key == 'project_list':
                    prompt += f"**Список проектов:**\n{value}\n\n"

    prompt += f"""

ТРАНСКРИПЦИЯ:
{transcription}
"""
    return prompt


