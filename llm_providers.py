                instruction = assignment.get('instruction', '')
                manager = assignment.get('manager_name', '')
                responsible = assignment.get('responsible', '')
                deadline = assignment.get('deadline', '')
                
                # ะคะพัะผะฐัะธััะตะผ ะฟะพัััะตะฝะธะต
