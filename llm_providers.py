def format_od_protocol(protocol_data: Dict[str, Any]) -> str:
    """
    –§–æ—Ä–º–∞—Ç–∏—Ä—É–µ—Ç OD –ø—Ä–æ—Ç–æ–∫–æ–ª –≤ —á–∏—Ç–∞–µ–º—ã–π —Ç–µ–∫—Å—Ç
    
    Args:
        protocol_data: –î–∞–Ω–Ω—ã–µ –ø—Ä–æ—Ç–æ–∫–æ–ª–∞ –∏–∑ ODProtocolSchema
        
    Returns:
        –û—Ç—Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–Ω—ã–π —Ç–µ–∫—Å—Ç –ø—Ä–æ—Ç–æ–∫–æ–ª–∞
    """
    result = []
    
    # –ó–∞–≥–æ–ª–æ–≤–æ–∫
    result.append("–ü–†–û–¢–û–ö–û–õ –ü–û–†–£–ß–ï–ù–ò–ô\n")
    result.append("=" * 60 + "\n")
    
    # –ú–µ—Ç–∞–¥–∞–Ω–Ω—ã–µ
    if protocol_data.get('meeting_date'):
        result.append(f"–î–∞—Ç–∞ –≤—Å—Ç—Ä–µ—á–∏: {protocol_data['meeting_date']}\n")
    
    if protocol_data.get('managers'):
        result.append(f"–†—É–∫–æ–≤–æ–¥–∏—Ç–µ–ª–∏: {protocol_data['managers']}\n")
    
    if protocol_data.get('participants'):
        result.append(f"–£—á–∞—Å—Ç–Ω–∏–∫–∏: {protocol_data['participants']}\n")
    
    result.append("\n" + "=" * 60 + "\n\n")
    
    # –ó–∞–¥–∞—á–∏ –∏ –ø–æ—Ä—É—á–µ–Ω–∏—è
    tasks = protocol_data.get('tasks', [])
    
    for i, task in enumerate(tasks, 1):
        task_name = task.get('task_name', '–ë–µ–∑ –Ω–∞–∑–≤–∞–Ω–∏—è')
        result.append(f"{i}. {task_name}\n")
        
        assignments = task.get('assignments', [])
        if assignments:
            for assignment in assignments:
                instruction = assignment.get('instruction', '')
                manager = assignment.get('manager_name', '')
                responsible = assignment.get('responsible', '')
                deadline = assignment.get('deadline', '')
                
